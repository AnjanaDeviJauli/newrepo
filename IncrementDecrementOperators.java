package com.perscholas.java_basics;

public class IncrementDecrementOperators {

	public static void main(String[] args) {
		int x=5;
		System.out.println(x);
		
		//Three ways to increment a variable
		x++;
		System.out.println(x);
		
		x +=1;
		System.out.println(x);
		
		++x;
		System.out.println(x);
		
		int a=5,b=8,sum=0;
		sum=a++ + b;
		System.out.println(sum);
		
		
	}

}
